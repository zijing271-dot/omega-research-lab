const https = require('https');
const http = require('http');

const TELEGRAPH_API = 'api.telegra.ph';

function telegraphRequest(method, params = {}) {
    return new Promise((resolve, reject) => {
        const qs = Object.entries(params)
            .map(([k, v]) => `${k}=${encodeURIComponent(v)}`)
            .join('&');
        const path = `/${method}?${qs}`;

        https.get({ hostname: TELEGRAPH_API, path }, (res) => {
            let data = '';
            res.on('data', chunk => data += chunk);
            res.on('end', () => {
                try {
                    const json = JSON.parse(data);
                    if (json.ok) resolve(json.result);
                    else reject(new Error(json.error || 'API error'));
                } catch (e) {
                    reject(e);
                }
            });
        }).on('error', reject);
    });
}

async function createAccount(shortName, authorName, authorUrl) {
    return telegraphRequest('createAccount', {
        short_name: shortName,
        author_name: authorName,
        author_url: authorUrl
    });
}

async function createPage(accessToken, title, content, authorName, authorUrl) {
    return telegraphRequest('createPage', {
        access_token: accessToken,
        title,
        author_name: authorName || 'OMEGA AI Research',
        author_url: authorUrl || '',
        content: JSON.stringify(content),
        return_content: 'true'
    });
}

// Get or create account token
async function getAccountToken() {
    const fs = require('fs');
    const tokenFile = `${__dirname}/.telegraph-token.json`;

    if (fs.existsSync(tokenFile)) {
        const saved = JSON.parse(fs.readFileSync(tokenFile, 'utf8'));
        if (saved.access_token && saved.auth_url) {
            console.log(`[OMEGA] Using saved Telegraph account: ${saved.auth_url}`);
            return saved;
        }
    }

    // Create new account
    const result = await createAccount('OMEGA_AI_Research', 'OMEGA AI Research', 'https://t.me/omega_ai_research');
    fs.writeFileSync(tokenFile, JSON.stringify(result, null, 2));
    console.log(`[OMEGA] Created new Telegraph account: ${result.auth_url}`);
    return result;
}

// Format content as Telegraph nodes
function telegraphContent(article) {
    const nodes = [];

    // Title
    nodes.push({ tag: 'h1', children: [article.title] });

    // Meta
    nodes.push({
        tag: 'p',
        children: [`📅 ${article.date} | 🔍 OMEGA AI Research | ⏱ ${article.readTime} min read`]
    });

    // Subtitle
    if (article.subtitle) {
        nodes.push({ tag: 'h3', children: [article.subtitle] });
    }

    // Sections
    for (const section of article.sections) {
        if (section.heading) {
            nodes.push({ tag: 'h2', children: [section.heading] });
        }
        if (section.subheading) {
            nodes.push({ tag: 'h4', children: [section.subheading] });
        }
        if (section.text) {
            if (Array.isArray(section.text)) {
                for (const t of section.text) {
                    nodes.push({ tag: 'p', children: [t] });
                }
            } else {
                nodes.push({ tag: 'p', children: [section.text] });
            }
        }
        if (section.bullets) {
            for (const b of section.bullets) {
                nodes.push({ tag: 'p', children: [`• ${b}`] });
            }
        }
        if (section.table) {
            // Simple table as text
            nodes.push({ tag: 'pre', children: [section.table] });
        }
    }

    // Disclaimer
    nodes.push({ tag: 'hr', children: [] });
    nodes.push({
        tag: 'p',
        children: ['⚠️ Disclaimer: This article is for informational purposes only and does not constitute financial, investment, or trading advice. Cryptocurrency and stock trading involves substantial risk of loss. Always do your own research before making investment decisions.']
    });

    // Footer
    nodes.push({
        tag: 'p',
        children: ['📡 Generated by OMEGA AI Research | Autonomous Content Factory']
    });

    return nodes;
}

